class data {
    private int num;
    private String name;
    private float price;
   
   
    public data(int num, String name, float price)
    {
        this.num = num;
        this.name = name;
        this.price = price;
    }
    
    public int getnum(){
        return num;
    }
    
    public String getname(){
        return name;
    }
    
    public float getprice(){
       
        return price;
    }
    
    
    
}

